package com.dyw.client.entity;

public class FunctionEntity {
    private int functionId;
    private String functionName;
    private String functionNote;
    private int functionSort;

    public int getFunctionId() {
        return functionId;
    }

    public void setFunctionId(int functionId) {
        this.functionId = functionId;
    }

    public String getFunctionName() {
        return functionName;
    }

    public void setFunctionName(String functionName) {
        this.functionName = functionName;
    }

    public String getFunctionNote() {
        return functionNote;
    }

    public void setFunctionNote(String functionNote) {
        this.functionNote = functionNote;
    }

    public int getFunctionSort() {
        return functionSort;
    }

    public void setFunctionSort(int functionSort) {
        this.functionSort = functionSort;
    }
}
